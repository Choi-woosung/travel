package com.travel.www.vo;

public class ScheduleDetailVO {
	
	private int sdno;
	private int sdday;
	private int sdono;
	private String type;
	private int sdotype;
	private String sdbody;
	
	public int getSdno() {
		return sdno;
	}
	public void setSdno(int sdno) {
		this.sdno = sdno;
	}
	public int getSdday() {
		return sdday;
	}
	public void setSdday(int sdday) {
		this.sdday = sdday;
	}
	public int getSdono() {
		return sdono;
	}
	public void setSdono(int sdono) {
		this.sdono = sdono;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getSdotype() {
		return sdotype;
	}
	public void setSdotype(int sdotype) {
		this.sdotype = sdotype;
	}
	public String getSdbody() {
		return sdbody;
	}
	public void setSdbody(String sdbody) {
		this.sdbody = sdbody;
	}
	
}
