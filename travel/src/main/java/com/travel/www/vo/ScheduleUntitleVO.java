package com.travel.www.vo;

public class ScheduleUntitleVO {
	
	private int uno;
	private String ufile;
	private int ucost;
	private String type;
	
	public int getUno() {
		return uno;
	}
	public void setUno(int uno) {
		this.uno = uno;
	}
	public String getUfile() {
		return ufile;
	}
	public void setUfile(String ufile) {
		this.ufile = ufile;
	}
	public int getUcost() {
		return ucost;
	}
	public void setUcost(int ucost) {
		this.ucost = ucost;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
}
